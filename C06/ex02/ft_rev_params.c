#include<unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = argc;
	while (j > 1)
	{
		while (argv[j - 1][i] != '\0')
		{
			c = argv[j - 1][i];
			write (1, &c, 1);
			i++;
		}
		i = 0;
		j--;
		write (1, "\n", 1);
	}
	return(0);
}
