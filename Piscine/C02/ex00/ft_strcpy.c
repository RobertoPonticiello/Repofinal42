#include<unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}	
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	src[] = "Forzanapoli";
	char	dest[100];

	ft_strcpy(dest, src);
	return (0);
}
