#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		write (1, "-2", 2);
		write (1, "147483648", 9);
	} 
	else if (nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
		ft_putnbr(nb);
	}
	else if (nb <= 9)
	{
		c = nb + '0';
		write (1, &c, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		c = (nb % 10) + '0';
		write (1, &c, 1);
	}
}
